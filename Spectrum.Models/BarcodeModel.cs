using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Linq;
using System.Text;

namespace Spectrum.Models
{
   public class BarcodeModel
    {        
           [DisplayName("Item Code")]
           public string  ItemCode { get; set; }

           [DisplayName("EAN")]
           public string EAN { get; set; }

           [DisplayName("Item Description")]
           public string ItemDescription { get; set; }

           [DisplayName("Barcode Type")]
           public string BarcodeType { get; set; }

           [DisplayName("No of Prints")]
           public int NoOfPrints { get; set; }

           [DisplayName("Exclude")]
           public bool  Exclude { get; set; }
       
    }
   public class BarcodeModelExcel
   {
       [DisplayName("Article Name")]
       public string ArticleName { get; set; }

       [DisplayName("Article Code")]
       public string ArticleCode { get; set; }

       [DisplayName("BARCODE")]
       public string BARCODE { get; set; }

       [DisplayName("Selling Price")]
       public Nullable<decimal> SellingPrice { get; set; }

       [DisplayName("MRP")]
       public Nullable<decimal> MRP { get; set; }

       [DisplayName("UOM")]
       public string UOM { get; set; }

       [DisplayName("Net Weight")]
       public Nullable<decimal> NetWeight { get; set; }

       [DisplayName("Expiry Date")]
       public string  ExpiryDate { get; set; }

   }
   public class DocumentModel
   {
       [DisplayName("Document Number")]
       public string DocumentNumber { get; set; }

       [DisplayName("Date")]
       public DateTime DocDate { get; set; }

       [DisplayName("Supplier")]
       public string SupplierName { get; set; }

       [DisplayName("Delivery Site Name")]
       public string SiteShortName { get; set; }

       [DisplayName("Status")]
       public string  OrderStatus { get; set; }

       [DisplayName("Doc. Net. Value")]
       public Nullable<decimal> DocNetValue { get; set; }

   }
}
